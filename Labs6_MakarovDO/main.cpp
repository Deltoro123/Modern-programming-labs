#include "Stroka.h"
#include <iostream>
#include <clocale>
using namespace std;

int main() {
    setlocale(LC_ALL, "Russian");  // для поддержки русского языка
    
    cout << "========================================" << endl;
    cout << "Лабораторная работа №6" << endl;
    cout << "Класс для работы со строками" << endl;
    cout << "========================================" << endl << endl;
    
    // ========== Тестирование конструкторов ==========
    cout << "=== Тестирование конструкторов ===" << endl;
    
    Stroka s1("qwert");           // конструктор из C-строки
    cout << "s1 = ";
    s1.vyvod();
    cout << ", длина = " << s1.dllina() << endl;
    
    Stroka s2;                    // конструктор по умолчанию
    cout << "s2 (по умолчанию) = ";
    s2.vyvod();
    cout << ", длина = " << s2.dllina() << endl;
    
    Stroka s3(s1);                // конструктор копирования
    cout << "s3 (копия s1) = ";
    s3.vyvod();
    cout << ", длина = " << s3.dllina() << endl;
    
    Stroka s4(100);               // конструктор с заданным размером
    cout << "s4 (размер 100) = ";
    s4.vyvod();
    cout << ", длина = " << s4.dllina() << endl;
    
    // ========== Тестирование ввода ==========
    cout << "\n=== Тестирование ввода ===" << endl;
    cout << "Введите строку для s2: ";
    s2.vvod();
    cout << "s2 = ";
    s2.vyvod();
    cout << ", длина = " << s2.dllina() << endl;
    
    // ========== Тестирование присваивания ==========
    cout << "\n=== Тестирование присваивания ===" << endl;
    s4 = s2;
    cout << "s4 = s2 -> s4 = ";
    s4.vyvod();
    cout << endl;
    
    // ========== Тестирование сцепления ==========
    cout << "\n=== Тестирование сцепления ===" << endl;
    Stroka s5 = s1 + s2 + s3;
    cout << "s5 = s1 + s2 + s3 = ";
    s5.vyvod();
    cout << ", длина = " << s5.dllina() << endl;
    
    // ========== Тестирование сравнения ==========
    cout << "\n=== Тестирование сравнения ===" << endl;
    if (s1 == s3) {
        cout << "s1 и s3 равны" << endl;
    }
    else {
        cout << "s1 и s3 не равны" << endl;
    }
    
    if (s1 == s2) {
        cout << "s1 и s2 равны" << endl;
    }
    else {
        cout << "s1 и s2 не равны" << endl;
    }
    
    // ========== Дополнительное тестирование ==========
    cout << "\n=== Дополнительное тестирование ===" << endl;
    Stroka s6;
    cout << "Введите еще одну строку: ";
    s6.vvod();
    
    Stroka s7 = s5 + s6;
    cout << "s5 + s6 = ";
    s7.vyvod();
    cout << endl;
    
    // ========== Тестирование цепочки операций ==========
    cout << "\n=== Тестирование цепочки операций ===" << endl;
    Stroka s8;
    s8 = s1;
    cout << "s8 = s1 -> s8 = ";
    s8.vyvod();
    cout << endl;
    
    Stroka s9 = s8 + s2;
    cout << "s9 = s8 + s2 = ";
    s9.vyvod();
    cout << endl;
    
    cout << "\n========================================" << endl;
    cout << "Программа завершена успешно!" << endl;
    
    return 0;
}